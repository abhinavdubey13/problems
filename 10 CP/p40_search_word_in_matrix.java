class Solution {

        int ROWS;
        int COLS;

        boolean function_util(char[][] arr, String word) {
                ROWS = arr.length;
                COLS = arr[0].length;

                for (int i = 0; i < ROWS; i++) {
                        for (int j = 0; j < COLS; j++) {
                                boolean temp = dfs(arr, word, i, j, word.length(), 0);
                                if (temp) {
                                        return true;
                                }
                        }
                }
                return false;
        }

        boolean dfs(char[][] arr, String word, int x, int y, int n, int str_idx) {

                if (str_idx == n) {
                        return true;
                }

                if (!is_safe(x, y)) {
                        return false;
                }

                char str_char = word.charAt(str_idx);
                char arr_char = arr[x][y];

                if (str_char != arr_char) {
                        return false;
                }

                arr[x][y] = '*';
                boolean a = dfs(arr, word, x + 1, y, n, str_idx + 1);
                boolean b = dfs(arr, word, x - 1, y, n, str_idx + 1);
                boolean c = dfs(arr, word, x, y + 1, n, str_idx + 1);
                boolean d = dfs(arr, word, x, y - 1, n, str_idx + 1);
                arr[x][y] = arr_char;
                return (a || b || c || d);
        }

        boolean is_safe(int x, int y) {
                return x >= 0 && x < ROWS && y >= 0 && y < COLS;
        }

}